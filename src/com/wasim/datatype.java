package com.wasim;
public class datatype {
    public static void main(String[] args) {
        int a = 2;
        System.out.println(a);
        a = 'c';
        System.out.println(a);
        long l = 45;
        System.out.println(l);
        l = 'w';
        System.out.println(l);
        float c = 34l;
        System.out.println(c);
        c = 'a';
        System.out.println(c);

    }
}
